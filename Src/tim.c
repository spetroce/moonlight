void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
  if(htim->Instance == TIM1) {
    if (g_zero_cross_count >= 3) {
      const uint16_t zero_cross_period =
        __HAL_TIM_GET_COMPARE(&htim1, TIM_CHANNEL_1);
      if (zero_cross_period > MIN_ZERO_CROSS_PERIOD &&
          zero_cross_period < MAX_ZERO_CROSS_PERIOD) {
        g_zero_cross_period = zero_cross_period;
        __HAL_TIM_SET_AUTORELOAD(&htim1, g_zero_cross_period);
        // We no longer need to measure the zero cross period so stop the
        // input capture interrupt. This will also prevent us from ever
        // entering this 'else if(htim->Instance == TIM1)' section again.
        HAL_TIM_IC_Stop_IT(&htim1, TIM_CHANNEL_1);
        // Once a proper zero cross period has been recorded, we can
        // begin switching the triac gate (turn the heater on).
        HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);
      } else {
        g_zero_cross_count = 0;
      }
    } else {
      ++g_zero_cross_count;
    }
  }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
  if (htim->Instance == TIM6) {
    g_tim_check_keypad = true;
  }
}
